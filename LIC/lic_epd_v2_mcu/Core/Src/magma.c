// Encrypting data
// plainSize, encryptedSize = 1 byte because LoraWAN has a limitation on data size of 222 bytes (DR7)
uint8_t encrypt(uint8_t * plainData, uint8_t plainSize, uint8_t * encryptedData, uint8_t * encryptedSize){
  // Maximum size of packet
  // The size is a multiple of 8, more than 216 does not make sense due to the limitation (222 bytes)
  uint8_t maxSize = 216;

  // The data size should be smaller by : counter (1 byte) + data size (1 byte)
  if (plainSize == 0 || plainSize > maxSize - 2)
  {
    return 1; // Size of plain data is too big
  }


  // 1. Checking the data size for multiplicity
  // Determination of the balance (additional filling)
  // The data size must be a multiple of 8 bytes
  uint8_t n = (plainSize + 2) % 8;
  if (n > 0) n = 8 - n;

  // Size of encrypted data (with counter (1 byte) + data size (1 byte))
  *encryptedSize = plainSize + n + 2;


  // 2. Adding additional data
  // Counter
  encryptedData[0] = CYPHER_COUNTER;
  CYPHER_COUNTER++;

  // Size of data
  encryptedData[1] = plainSize;

  // Copying data to an encrypted buffer
  for (uint8_t i = 0; i < plainSize; ++i)
  {
    encryptedData[2 + i] = plainData[i];
  }

  // Data Addition
  for (uint8_t i = 0; i < n; ++i)
  {
    // Data Addition
    uint8_t b = rand() % 255;

    encryptedData[2 + plainSize + i] = b;
  }


  // 3. Encrypting
  uint32_t tmp[2];

  for (uint8_t i = 0; i < (*encryptedSize / 8); ++i)
  {
    uint32_t * ptr = (uint32_t *)encryptedData;

    tmp[0] = ptr[2 * i];
    tmp[1] = ptr[2 * i + 1];

    Simple_Cypher(Key, uzly, tmp);

    ptr[2 * i]     = tmp[0];
    ptr[2 * i + 1] = tmp[1];
  }
  
  return 0; // OK
} // encrypt


// Decrypting data
// encryptedSize, plainSize = 1 byte because LoraWAN has a limitation on data size of 222 bytes (DR7)
uint8_t decrypt(uint8_t * encryptedData, uint8_t encryptedSize, uint8_t * plainData, uint8_t * plainSize){
  // Maximum size of packet
  // The size is a multiple of 8, more than 216 does not make sense due to the limitation (222 bytes)
  uint8_t maxSize = 216;

  if (encryptedSize == 0 || encryptedSize > maxSize)
  {
    return 1; // Size of encrypted data is too big
  }

  // The data size (with counter (1 byte) + data size (1 byte)) 
  // must be a multiple of 8 bytes
  if (encryptedSize % 8 > 0)
  {
    return 2; // Size of encrypted data is not a multiple of 8 bytes
  }


  // 1. Copying data to the decrypted buffer
  for (uint8_t i = 0; i < encryptedSize; ++i)
  {
    plainData[i] = encryptedData[i];
  }


  // 2. Decrypting data
  uint32_t tmp[2];

  for (uint8_t i = 0; i < (encryptedSize / 8); ++i)
  {
    uint32_t * ptr = (uint32_t *)plainData;

    tmp[0] = ptr[2 * i];
    tmp[1] = ptr[2 * i + 1];

    Simple_DeCypher(Key, uzly, tmp);

    ptr[2 * i]     = tmp[0];
    ptr[2 * i + 1] = tmp[1];
  }


  // 3. Definition of additional data
  // Counter
  CYPHER_COUNTER = plainData[0];

  // Size of data
  *plainSize = plainData[1];


  // 4. Data offset
  for (uint8_t i = 0; i < *plainSize; ++i)
  {
    plainData[i] = plainData[2 + i];
  }
  
  return 0; // OK
} // decrypt